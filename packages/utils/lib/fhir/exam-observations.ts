export const parseMusculoskeletalFieldToName = (field: string): string => {
  const arr = field.split('-');
  const resultArr: string[] = [];

  resultArr.push(musculoskeletalSideOptions.find((option) => option.value === arr[1])!.label);
  resultArr.push(musculoskeletalBodyPartOptions[arr[2] as keyof typeof musculoskeletalBodyPartOptions]);

  switch (arr[2]) {
    case 'finger':
      resultArr.push(musculoskeletalFingerOptions[arr[3] as keyof typeof musculoskeletalFingerOptions]);
      break;
    case 'toe':
      resultArr.push(musculoskeletalToeOptions[arr[3] as keyof typeof musculoskeletalToeOptions]);
      break;
  }

  resultArr.push(musculoskeletalAbnormalOptions[arr[0] as keyof typeof musculoskeletalAbnormalOptions]);

  return resultArr.join(' | ');
};

export const convertTemperature = (value: string, to: 'celsius' | 'fahrenheit'): string => {
  const temperature = parseFloat(value);
  if (!isNaN(temperature)) {
    const converted = to === 'celsius' ? ((temperature - 32) * 5) / 9 : (temperature * 9) / 5 + 32;
    return converted.toFixed(2);
  } else {
    return '';
  }
};
