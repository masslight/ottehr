export const parseRashesFieldToName = (fieldName: string, fields: ExamObservationDTO[]): string => {
  const resultArr: string[] = [];

  resultArr.push(rashesOptions[fieldName as keyof typeof rashesOptions]);

  const field = fields.find((field) => field.field === fieldName)!;

  if (field.note) {
    resultArr.push(field.note);
  }

  return resultArr.join(' | ');
};

